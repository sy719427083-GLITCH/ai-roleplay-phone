const point = (x, y) => ({ x, y });

const pathFromPairs = (pairs) => pairs
  .map(([x, y], index) => `${index === 0 ? "M" : "L"} ${x} ${y}`)
  .join(" ");

const pointsFromPairs = (pairs) => pairs.map(([x, y]) => point(x, y));

const route = (distanceMeters, samplePairs, visibleSegmentPairs = [samplePairs]) => {
  const samples = pointsFromPairs(samplePairs);
  return {
    pin: { ...samples.at(-1) },
    distanceMeters,
    samples,
    visibleSegments: visibleSegmentPairs.map(pathFromPairs),
  };
};

