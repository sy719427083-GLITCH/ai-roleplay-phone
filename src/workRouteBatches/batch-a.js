const point = (x, y) => ({ x, y });

const formatNumber = (value) => (
  Number.isInteger(value) ? String(value) : String(value).replace(/0+$/, "").replace(/\.$/, "")
);

const formatPoint = ({ x, y }) => `${formatNumber(x)} ${formatNumber(y)}`;

const buildPath = (samples) => (
  `M ${formatPoint(samples[0])}${samples.slice(1).map((sample) => ` L ${formatPoint(sample)}`).join("")}`
);

const buildVisibleSegments = (samples, breakIndices = []) => {
  const segments = [];
  let startIndex = 0;

  for (const breakIndex of breakIndices) {
    const segmentSamples = samples.slice(startIndex, breakIndex + 1);
    if (segmentSamples.length >= 2) segments.push(buildPath(segmentSamples));
    startIndex = breakIndex;
  }

  const trailingSamples = samples.slice(startIndex);
  if (trailingSamples.length >= 2) segments.push(buildPath(trailingSamples));
  return segments;
};

const route = (pin, distanceMeters, samples, breakIndices = []) => ({
  pin,
  distanceMeters,
  samples,
  visibleSegments: buildVisibleSegments(samples, breakIndices),
});

